/**
 * The main.NotEqualSizeOfGroups exception is thrown when there is an inappropriate number of people
 * that cannot be divided into equal groups.
 */
public class NotEqualSizeOfGroups extends Exception {
    public NotEqualSizeOfGroups() {
        super();
    }
    public NotEqualSizeOfGroups(String message) {
        super(message);
    }

}
